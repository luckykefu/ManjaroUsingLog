#!/bin/bash
# >>> 移动文件/目录并创建符号链接（安全版本）>>>
mv_and_ln(){
    # >>> 检查参数数量和源路径是否为符号链接 >>>
    [[ $# -ne 2 || -L "$1" ]] && return
    
    # >>> 创建目标路径的父目录 >>>
    mkdir -p "$(dirname "$2")"
    
    # >>> 仅当源存在且目标不存在时才移动 >>>
    [[ -e "$1" && ! -e "$2" ]] && mv "$1" "$2"
    
    # >>> 创建从源路径到目标路径的符号链接 >>>
    ln -sf "$2" "$1"
}
